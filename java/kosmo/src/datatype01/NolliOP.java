package datatype01;

public class NolliOP {
	public static void main(String[] args) {
	
	
	/*
	 * && 논리합 and
	 * || 논리곱 or
	 * ! 부정
	 * &, | 비트 연산자 (and or)
	 * 1] b1&& b2 :  b1과b2 모두가 true 일때만 true 나머지는 false 
	 *    단 b1이 false 이면 b2계산 안하고 false 리턴
	 *    
	 * 2] b1||b2 : b1과 b2가 모두가 거짓일때 거짓이고 나머지는 참
	 * 		단 b1이 참이면 b2는 무시하고 참
	 * 
	 * 3] b1&b2 둘다 참일때 참 나머지는 거짓 둘다 수행 
	 * 4] b1 |b2 둘다 거짓이면 거짓 나머지는 참 둘다 수행 
	 * 
	 * 
	 * 
	 */
	
	
		boolean b1 = (5>3) && (2>3);
		boolean b2 = (5>3) || (5<2);
		
		
	System.out.println();
	
	
	
	
	
	
	
	}
	
}
